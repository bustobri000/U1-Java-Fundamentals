public class Average2 {
    /*
    Given two numbers, num1 and num2, output the average.
    */
    public static void main(String[] args) {
        int num1 = 0;
        int num2 = 2;

        double numAverage = num1 + num2;
        double average = numAverage / 2.0;

        System.out.println(average);
    }
}
